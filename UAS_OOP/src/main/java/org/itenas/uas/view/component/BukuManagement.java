    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        String id_buku;
        bukuService = new BukuServiceImpl();
        int dialogButton = JOptionPane.YES_NO_OPTION;
        
        id_buku = txt_id_buku.getText();
        
        int dialogResult = JOptionPane.showConfirmDialog (null, "Apakah anda yakin untuk menghapus?", "Warning", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION){
            bukuService.delete(id_buku);
            JOptionPane.showMessageDialog(null, "Data buku berhasil di hapus!");
            loadData();
            emptyField();
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

