    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        String id_buku, judul, pengarang, penerbit, tahunTerbit, status;
        double harga;
        bukuService = new BukuServiceImpl();
        
        id_buku = txt_id_buku.getText();
        judul = txt_judul.getText();
        pengarang = txt_pengarang.getText();
        penerbit = txt_penerbit.getText();
        tahunTerbit = txt_tahunTerbit.getText();  
        harga = Double.parseDouble(txt_harga.getText());
        status = stat;
        
        Buku buku = new Buku();
        buku.setId(id_buku);
        buku.setJudul(judul);
        buku.setPengarang(pengarang);
        buku.setPenerbit(penerbit);
        buku.setTahunTerbit(tahunTerbit);
        buku.setHarga(harga);
        buku.setStatus(status);
        
        bukuService.create(buku);
        JOptionPane.showMessageDialog(null, "Data buku berhasil dibuat!");
        loadData();
        emptyField();
    }//GEN-LAST:event_btn_addActionPerformed

